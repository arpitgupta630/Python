# printing \\
print(r"This is \\ Double Backslashes")     # Raw string Method
print("This is \\\\ Double Backslashes\n")    # Simple Method

# printing /\/\/\/\/\ (Mountains)
print(r"These are /\/\/\/\/\ Mountains")        # Raw String Method
print("These are /\\/\\/\\/\\/\\ Mountains\n")    # Simple Method

# printing Escape Sequences
print(r"\" \n \t \' ")      # Raw String Method
print("\\\" \\n \\t \\'")   # Simple Method
